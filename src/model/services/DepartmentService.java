package model.services;

import java.util.List;

import model.dao.DaoFactory;
import model.dao.DepartmentDao;
import model.entities.Department;

public class DepartmentService {
	
	private DepartmentDao dao = DaoFactory.createDepartmentDao(); //injeta a dependencia usando o padrao Factory
		
	public List<Department> findAll() { //funcao ou método - findAll
		return dao.findAll(); //vai buscar os departamentos no banco de dados
	}
	
	public void saveOrUpdate(Department obj) {
		if (obj.getId() == null) {
			dao.insert(obj);
		}
		else {
			dao.update(obj);
		}
	}
	
}
